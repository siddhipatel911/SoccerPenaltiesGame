    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String name = txtPlayerName.getText().trim().toLowerCase(); //gets the player name input and trims it and makes it lower case to make sure there are no errors in input
        
        if (name.isEmpty()) { //if not name is entered
            JOptionPane.showMessageDialog(this, "Enter a player name to search."); //displays an error message
            return;
        }
        
        try {
            BufferedReader reader = new BufferedReader(new FileReader("highscores.txt")); //accesses the file
            StringBuilder results = new StringBuilder(); //creates a string for the result
            String line;

            while ((line = reader.readLine()) != null) { //reads file by line
                if (line.toLowerCase().startsWith(name + " -")) { //looks for first part of the line (name -)
                    results.append(line).append("\n"); //creates a new line if the player has more than one saved score to display them all
            }
        }
        
        reader.close(); //closes file

            if (results.length() == 0) { //if no reults found
                txtScoresList.setText("No scores found for: " + name); //displays appropriate message
            } 
            else { //if results found
                txtScoresList.setText(results.toString()); //displays name and scores
            }
        } 
        catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading highscores: " + e.getMessage()); //error message if file is not read properly
        }
    }//GEN-LAST:event_btnSearchActionPerformed

