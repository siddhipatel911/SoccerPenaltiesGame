    private void btnSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortActionPerformed
        try {
            BufferedReader reader = new BufferedReader(new FileReader("highscores.txt")); //accesses the text file
            List<String> lines = new ArrayList<>(); //creates an ArrayList for each line in the text file
            String line;

            /*Loads all the lines into the list*/
            while ((line = reader.readLine()) != null) {
                lines.add(line);
        }

        reader.close(); //closes the file
        
       // Sorts by score descending, then name ascending
       Collections.sort(lines, new Comparator<String>() {
        public int compare(String a, String b) {
                /*Splits up name and score*/
                String[] partsA = a.split(" - ");
                String[] partsB = b.split(" - ");
                
                int scoreA = Integer.parseInt(partsA[1]);
                int scoreB = Integer.parseInt(partsB[1]);

            /*First sorts by score descending*/
                int scoreCompare = Integer.compare(scoreB, scoreA); // descending
                if (scoreCompare != 0) return scoreCompare;

            /*Then sorts by name ascending*/
            return partsA[0].compareToIgnoreCase(partsB[0]);
         }
        });
        
        /* Displays sorted result*/
        StringBuilder sortedText = new StringBuilder();
        for (int i = 0; i < lines.size(); i++) {
            sortedText.append(lines.get(i)).append("\n"); //line by line
        }

        txtScoresList.setText(sortedText.toString()); //displays in text area box

        } 
        catch (IOException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error sorting highscores: " + e.getMessage()); //error message if file is not read properly
        }
    }//GEN-LAST:event_btnSortActionPerformed


