    /**
     * Loads and displays all scores from the highscores.txt file into the JTextArea.
     * Each line of the file is expected in the format "Name - Score".
     */
    public void loadHighScores() {
    File file = new File("highscores.txt"); //access the file
    try (BufferedReader br = new BufferedReader(new FileReader(file))) {
        String line;
            while ((line = br.readLine()) != null) { //reads each line
                txtScoresList.append(line + "\n"); // Displays in JTextArea
            }
        } 
    catch (IOException e) {
        e.printStackTrace();
        }
    }
    
